<p><em>Хомутынник, который во времена "семьи" вовсю решал вопросы с Таможней&nbsp; и Налоговой, и даже (по некоторым данным) контролировал экспорт орехов и импорт мяса, а уже во времена Билоуса курировал вопросы возмещения НДС, вот уже пару недель <a href="/news/2015/02/24/530325/">рассказывает во всех СМИ, что он оказывается всегда боролся с коррупцией</a>. Депутаты требуют разобраться с вопиющими фактами (до этого ничего не слышали про контрабанду?).</em></p>
<p><em>И вообще, складывается впечатление, что власть наконец-то решила очиститься от ужасных фактов, о которых не знала.</em></p>
<p><em>Так вот, на все это есть один ответ &ndash; знала. И ее это устраивало. ЭП и другие СМИ весь прошлый год рассказывали о том, что Налоговая и Таможня отнюдь не стали чище. Более того, на фоне громких лозунгов о борьбе со схемами (которые делал тот же Яценюк), эти самые схемы &ndash; "обнал", "налоговые ямы" и прочие &ndash; процветали. И процветают.</em></p>
<p><em>Есть люди, которые курируют отдельные стороны жизнедеятельности Налоговой и Таможни. Они ничего не собираются сворачивать. Более того, во времена пост-Майдана в Украине процветает поставка горючего без уплаты акцизов в объемах, которые не снились легендарному "золотому мальчику" Сергею Курченко. Кто-то пытается рассказать о деятельности кустарных НПЗ, а кто-то прямо говорит о контрабанде российских нефтепродуктов через территорию АТО.</em></p>
<p><em>На этом фоне, ГФС добивает легальный сектор экономики и население, внося в парламент разработанные совместно с Минфином "антикризисные" законопроекты о повышении налогов.</em></p>
<p><em>Обо всем этом не знал Арсений Петрович? Очень даже знал. Наш премьер какой угодно, только не глупый, и не жертва манипуляций. Наверняка знали об этом и в Администрации президента, и во многих других местах.</em></p>
<p><em>Поэтому последнее выступление Яценюка нужно оценивать с юмором. Скорее всего, состоялась новая договоренность на самом высоком уровне. Силы, которые сегодня контролируют процессы во власти, решили сменить людей, которые будут контролировать Налоговую и Таможню. Нет, не для искоренения коррупции и ужасных вещей. Просто для того, чтобы по-новому "решать вопросы".</em></p>
<p><em>Билоус, Хоменко и Макаренко, скорее всего, уже не вернутся на свои позиции. Такова традиция подобных скандалов &ndash; хотя, конечно, всякое может быть.</em></p>
<p><em>Остается открытым вопрос, кому и в чем помешал Макаренко. Версий тут множество, и некоторые из них указывают на великолепный стиль Игоря Коломойского. Вот две детали. Первая - Хомутынник после ухода "семьи" сблизился с губернатором Днепропетровщины. Хотя вряд ли они были сильно далеки и год назад. Вторая &ndash; для назначения главы таможни в Одессе нужно одобрение губернатора Одесской области. А это, напомним, правая рука Коломойского &ndash; Игорь Палица. Это уже неплохая версия, которая предполагает хорошо разыгранный псевдоконфликт вокруг смс-ки Хомутынника. Хотя, конечно, могут быть и другие версии.</em></p>
<p><em>И еще одно. Накануне скандала в Кабмине глава ГФС Игорь Билоус подписал приказ №84. Этим документом он сделал Макаренко главой Таможни, подчиненным непосредственно себе. То есть, в обход первого заместителя ГФС Владимира Хоменко, которого относят к группе влияния Яценюка.</em></p>
<p><em>Интервью ЭП с Анатолием Макаренко состоялась в первой декаде февраля &ndash; как только появилась смс-ка Хомутынника. Глава Таможни оказался на удивление толерантным и сдержанным. Даже в отношении Хомутынника и Науменко. Также он отстаивал Мухина, упомянутого в смс.</em></p>
<p><em>Он произвел впечатление действительно адекватного руководителя Таможни. Который знает, что контрабанда и коррупция были и есть, и просто так они не исчезнут. Макаренко не блистает популизмом, обещая искоренить зло &ndash; этим он выгодно отличается от постмайданных политиков, которые говорят о ценностях революции, но продолжают зарабатывать на "схемах". Вместо популизма, Макаренко предлагает договариваться с местными элитами, которые в любом случае контролируют таможни на местах &ndash; чтобы те отдавали намного больше денег в госбюджет.</em></p>
<p><em>Признаться, мы не защищаем Макаренко и не утверждаем, что он чист и безгрешен. Но мы искренне опасаемся, что его вполне трезвые признания возьмут на вооружение крикуны и пропагандисты. Которые поспешат заклеймить позором руководителя, который даже не пытается обещать нереальное и демонстрировать революционную бодрость. И тем самым сыграют на руку тем, чьи интересы зацепил Макарено.</em></p>
<p><em>Но возможно, теперь это не так и важно. Судя по действиям Яценюка, решения уже приняты. И это явно не единоличное решение Арсения Петровича, а плод коллективного творчества.</em></p>
<p style="text-align: center;">***</p>
<p><strong>- Давайте начнем с проблемной точки - <a href="http://www.pravda.com.ua/news/2015/02/6/7057690/">смс</a>, которое писал Виталий Хомутынник. Хотя он утверждает, что не писал ее. Если это атака против вас или желание вас заменить, то в чьих это интересах?</strong></p>
<p>- Таможня всегда была объектом особого внимания не только политиков, но и финансово-промышленных групп. Поэтому, если возникает похожая ситуация, то сразу нужно искать ответ на вопрос, кому это необходимо.</p>
<p>Или ты кому-то помешал, или плохо делаешь работу, или выгодно тебя сменить, потому что кто-то получит преференции благодаря тому, что на твое место придет другой. За восемь месяцев своей "каденции", наверное, помешал многим.</p>
<p><strong>- А что вы сделали? Какие проблемные точки?</strong></p>
<p>- Проблемные точки - это, прежде всего, энергетика, где таможня заняла жесткую позицию в плане контроля над энергоносителями. Это прежде всего светлые нефтепродукты и другие энергоносители.</p>
<p>У нас есть например, трубопровод, который идет из России на Ужгород через Белоруссию. Там больше 100 тыс тонн технологического дизеля, и его пытались скачать.</p>
<p><strong>- Говорили, что этим группа Игоря Еремеева занимается.</strong></p>
<p>- Откровенно скажу: я не знаю, кто стоит за этим, какая финансово-промышленная группа. Но ситуация складывалась таким образом: пытались этот технологический продукт вытеснить воздухом, предоставляя к тому же документы, что будет замещение другим продуктом. Мы это все остановили.</p>
<p>Имели жесткую проблему, вплоть до силовых вариантов по Закарпатью, когда пломбировали там краны. Это было после новогодней ночи.</p>
<p><strong>- Это когда захватили какую-то насосную станцию?</strong></p>
<p>- Да. Тогда подключились органы милиции, мы им благодарны. Мы опломбировали и эту выкачку запретили. Сейчас дело в судах, не знаю продолжения. Мое дело было остановить эту выкачку. Более 100 тыс тонн остались в трубе. Технологический режим не нарушен.</p>
<p><strong>- Кому принадлежали эти нефтепродукты?</strong></p>
<p>- Трубопровод принят в распоряжение государства, а по технологическому продукту идут суды. А пока идут суды, хотели продукт оттуда вытолкнуть. Но он должен находиться постоянно в трубопроводе согласно технологическим требованиям.</p>
