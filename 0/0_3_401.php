<p><em><span class="buk">2</span>4 февраля 2015 года на Кабмине разыгралось невиданное представление. Премьер Арсений Яценюк сделал вид, что больше не может спокойно смотреть на отвратительную работу Государственной фискальной службы. Дескать, поначалу Налоговая и Таможня работали как надо, а потом в них вернулись коррупция, схемы и прочие вторичные признаки разложения.</em></p>
<p><em>По итогам заседания, на время служебного расследования от исполнения обязанностей были <a href="/news/2015/02/24/530314/">отстранены все три руководителя ГФС</a> &ndash; Игорь Билоус (руководитель ГФС и Налоговой в частности, считается протеже то ли Леонида Юрушева, то ли Игоря Коломойского), Владимир Хоменко (глава налоговой милиции, протеже Яценюка) и Анатолий Макаренко (глава Таможни).</em></p>
<p><em>Относительно того, кто продвинул на этот пост Анатолия Макаренко, слухов не так и много. За неимением особых версий, вспоминают &ndash; дескать, Макаренко в свое время помог Юлии Тимошенко растаможить газ, отобранный у Дмитрия Фирташа. И даже отсидел за это почти год. Поэтому, якобы в благодарность за сделанную услугу, экс-премьер договорилась отдать таможню заслуженному человеку.</em></p>
<p><em>Пожалуй, на этом все. Зато в последние три недели стало очевидно, что Макаренко мешает очень многим. Сначала по интернету разошлась <a href="/rus/news/2015/01/26/523235/">фейковая новость</a> о том, что профильный парламентский комитет выразил недовольство Макаренко.</em></p>
<p><em>Имеется в виду якобы случайно сфотографированную переписку депутата Виталия Хомутынника с губернатором Одесской области Игорем Палицей. В ней оба персонажа, особо ничем не рискуя, якобы обсуждают ужасное положение дел на одесской таможне, где промышляют некий "Макар" (очевидно, намек на Макаренко) и "Мухин" (директор департамента анализа рынков и противодействия таможенным правонарушениям Александр Мухин, подчинен непосредственно главе Таможни).</em></p>
<p><em>И вот, фарс начал набирать обороты. Первым Макаренко поддел его предшественник &ndash; Виталий Науменко. Он прокомментировал новость об смс-ке Хомутынника в духе "ах ты!" Хотя, напомним, именно при Науменко произошла резкая просадка поступлений в госбюджет от работы Таможни. По слухам, гулявшим в то время по тогда еще Миндоходов, на местах резко возросла доля "серого" импорта и контрабанды &ndash; таможенники спешили заработать во время периода междувластия. И это факт, даже если Науменко действительно "послал на три буквы Хомутынника" (так объясняют причину его отставки).</em></p>
