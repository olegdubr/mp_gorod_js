<p><strong>- Что вы хотите предложить?</strong></p>
<p>- Не хотел бы говорить о деталях, но работа этих крупных предприятий показала, что есть проблемы. Просто пока три точки поставлю.</p>
<p><strong>- Вы фиксируете случаи законного-незаконного перемещения крымских товаров через территорию Украины за рубеж?</strong></p>
<p>- Нет, мы не фиксировали. У нас не было таких фактов.</p>
<p>На территорию Украины въезжают только товары из свободной экономической зоны. Те товары, которые получают в Торгово-промышленной палате сертификат У1 - сертификат, подтверждающий их происхождение в Украине, освобождаются от ввозной пошлины.</p>
<p>Если есть такой сертификат, товар может быть размещен в любой таможенный режим. Посчитает нужным субъект внешнеэкономической деятельности продать - он может продать их за пределы Украины. В таком случае товары оформляются таможней в режиме реэкспорта.</p>
<p><strong>- Торгово-промышленная палата выдает такие сертификаты?</strong></p>
<p>- Раньше выдавала. Возникла ситуация мощного давления на украинские предприятия, которые остались там. От них требовали перерегистрации, вот здесь коллизия и возникла. Мы с Минфином будем инициировать пересмотр некоторых норм закона, связанных с Крымом.</p>
<p><strong>- Есть ли такая проблема, что предприятия, зарегистрированные на территории Крыма и зоны АТО, ввозят товары не через территорию Украины?</strong></p>
<p>- Не исключаю. К сожалению, у нас нет достоверной информации. Есть информация, что товары ввозятся через территорию России. Это не подтвержденная информация, поскольку там нет наших подразделений.</p>
<p><strong>- Это может стать проблемой?</strong></p>
<p>- Это может быть проблемой. Для этого усиливаем режим на линии разграничения.</p>
<p>На перешейке смогли наладить контроль. Начинали с похожей ситуации. Она, конечно, была намного проще географически, и войны, слава богу, не было. Контроль наладили и четко понимаем, что заходит и что выходит из Крыма.</p>
<p><strong>- Это полномочия налоговой милиции?</strong></p>
<p>- Мы, таможенники, имеем опыт, уже создавали границу с Приднестровьем. Так же охраняем и другие границы. Понимаем, что такое контролировать товар на разграничении. Это не граница, но знаем, что есть механизмы контроля товаров. Мы в одном ведомстве, это облегчает взаимодействие.</p>
<p style="text-align: center;">***</p>
<p><strong>- На уровне публичных выступлений вы были сторонником более автономного сильного ведомства в виде таможни по полномочиям в борьбе с контрабандой. Какие вам нужны полномочия в военных условиях?</strong></p>
<p><strong>Как вы общаетесь с первым заместителем главы ГФС Владимиром Хоменко по распределению полномочий между налоговой милицией и таможней?</strong></p>
<p>- У нас нет никаких противоречий в плане функционального распределения. За борьбу с криминальными правонарушениями отвечает налоговая милиция. Административные правонарушения, декриминализированное изъятие предметов таможенных правонарушений, - ответственность таможенной составляющей. Нам это удалось решить в октябре 2014 года.</p>
<p>-&nbsp;<strong>Какую роль играет СБУ? Писали, что в Одессе она "крышует"...</strong></p>
<p>- Вокруг таможни всегда было много желающих покормиться из "черного" "нала". В 2010 году создали вертикаль, лифт: выработали некие "черные" правила, создали некие "черные" кассы и начали двигаться некие "черные" чемоданы.</p>
<p><strong>- А раньше?</strong></p>
<p>- Раньше, по крайней мере, в 2009 году, этого не было. Вокруг таможни не крутилось столько желающих "поживиться". Так называемая "черная" вертикаль рухнула, но потом все начали собираться в маленькие местные пирамидки.</p>
<p><strong>- Кто лишний на границе?</strong></p>
<p>- Лишние все, кроме пограничников и таможни. Таможня должна минимально заниматься налогами. Таможня должна заниматься контролем товаров. Мы, к сожалению, занимаемся бюджетом и налогами. Сколько в Японии составляют таможенные платежи в бюджет? 2%.</p>
