<p>- Это близко к той истории. Там было много товарных партий, но меня как таможенника это не интересует.</p>
<p><strong>- А они были не растаможены?</strong></p>
<p>- Они были не оформлены в свободное обращение. Их пытаются вывезти из зоны таможенного контроля, прикрываясь благими намерениями, решениями районного суда Одессы.</p>
<p>Мы заняли позицию: "Коллеги, все что оформлено, - пожалуйста. Остальные 8 тыс тонн - не отдадим. Или под таможенным контролем: берем автоматчиков и сопровождаем до того места, куда вы хотите слить, и снова накладываем таможенную печать". Был серьезный конфликт. Мы устояли.</p>
<p><strong>- Существуют ли еще какие-то нефтепродукты "имени Курченко", задействованные в контрабандных схемах?</strong></p>
<p>- Существовала уникальная так называемая транзитная схема. Груз заходил в Украину, и потом заявлялось, что он транзитом шел на другую территорию, по документам вроде как находясь какое-то время на складе. Хотя, нефтепродукты без уплаты налогов реализовывались в Украине. Эту схему уничтожили еще в марте.</p>
<p>Потом некоторое время шлейфом работала схема, когда корабли заходили в Черное море, с них сливали топливо в районе Рени, Измаила. Эту схему мы тоже "убили". Это вопрос на стыке таможенников, пограничников и Службы безопасности. Совместными усилиями ликвидированы и эти "схемы" ухода от налогов.</p>
<p><strong>- Завершая этот вопрос: какие-то трения с Игорем Коломойским вы имели?</strong></p>
<p>- Никогда.</p>
<p><strong>- Может, он быть инициатором этой истории?</strong></p>
<p>- Мы лично не знакомы, и я абсолютно никаких трений с Игорем Валерьевичем никогда не имел.</p>
<p><strong>- Хомутынник считается приближенным к Коломойскому. Кто заинтересован, чтобы эта история развернулась?</strong></p>
<p>- Виталий Хомутынник всегда был самостоятельным политиком, тем более, на поле таможни и налоговой администрации. Сразу после известных событий я написал письмо Виталию Юрьевичу и Игорю Петровичу Палице. Попросил пояснить, что за этим стоит. Пока ответа нет. А Хомутынник после этого сделал известное заявление.</p>
<p><strong>- У вас фактов никаких нет?</strong></p>
<p>- Если вы помните, там написано было "Макар", моя фамилия Макаренко. Так вот: по-мужски я задет. Я гордый человек. Если сокращать фамилию, будет обидно. Моя мама очень переживает за меня. Она перезвонила и сказала: "Що це робиться? Тебе ображають". Я говорю: "Мамо, не хвилюйся, все гаразд". Но это неприятно.</p>
<p>Как чиновник я отреагировал. Написал одно письмо, другое. Предложил публично обсудить этот вопрос. Обратился к комитету налоговой и таможенной политики, куда входит и Виталий Юрьевич. Попросил предоставить факты, если они есть.</p>
<p>Также я обратился к Игорю Билоусу (<em>глава ГФС</em> - <em>ЭП</em>) и попросил назначить расследование в отношении второго, якобы фигуранта - Александра Мухина.</p>
<p><strong>- Это Билоус должен делать, не вы?</strong></p>
<p>- Нет, я заместитель руководителя ведомства. У меня нет подразделения собственной безопасности, я не назначаю кадры. Я могу вносить предложения.</p>
<p>И последнее. Я офицер. Первый раз одел погоны в 15 лет. В свое время поступил в Суворовское училище. Потом поступил в военное и закончил его. Вся сознательная жизнь &ndash; в погонах. Я знаю, что такое честь и, что такое отвечать за поступки.</p>
