<h1 class="title">Заступник голови ДФС Анатолій Макаренко: Навколо митниці завжди було багато охочих покормитися</h1>
<span class="dt2"><b>Галина Калачова, Сергій Лямець</b> &#151; Вівторок, 24 лютого 2015, 17:29</span>
