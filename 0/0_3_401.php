<p><strong>- А в чем смысл таможенных пунктов?</strong></p>
<p>- Это не таможенные пункты &ndash; это фискальные посты, если хотите.</p>
<p><strong>- То есть там нет пошлин, растаможки?</strong></p>
<p>- Нет, таможни там нет. Вы говорите со мной, я зампредседателя фискальной службы, который курирует работу таможни. Получилось так, что еще и этим занимаюсь, поскольку у нас опыт есть в работе на границе.</p>
<p><strong>- 30 января было принято постановление о введении специального приграничного режима.</strong></p>
<p>- Да, там находятся наши пограничники.</p>
<p><strong>- Как это будет функционировать? Смещение приграничной линии - это смещение государственной границы?</strong></p>
<p>- Ни в коем случае, одно с другим не связано. Никакого смещения госудаственной границы. Это просто усиление пропускного режима. Об этом вам лучше расскажут пограничники.</p>
<p><strong>- Но таможенники будут присутствовать?</strong></p>
<p>- Нет, только в тех случаях, если есть признаки внешнеэкономической операции. Мы выступаем как эксперты. По линии&nbsp; разграничения будут находиться сотрудники налоговой милиции.</p>
<p><strong>- А что может служить признаками?</strong></p>
<p>- Если груз пришел из Польши и на него нет документов, подтверждающих, что он прошел таможенную очистку, тогда таможенники включаются в работу. Если предъявляется декларация, оформлено все, налоговый милиционер говорит "ок", и груз проходит. Всем остальным, включая линию разграничения, таможня пока не занимается.</p>
<p><strong>- Как выглядит работа таможни на границе с Крымом?</strong></p>
<p>- Я получаю доклады по Крыму утром и вечером. В начале февраля российская сторона ужесточила режим. Один автомобиль из Украины они отрабатывают в течение часа. В сутки отрабатывают около 30 автомобилей. Появились очереди.</p>
<p>Что идет в Крым? Продукты, стройматериалы, бытовая химия. Поскольку россияне ввели декларирование на товары, то многие мелкие предприниматели перестали работать, сейчас работают в основном крупные производители.</p>
<p><strong>- А Украина фиксирует эти предприятия как экспортно-импортные?</strong></p>
