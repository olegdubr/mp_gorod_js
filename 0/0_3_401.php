<p><strong>- В Европе 5-10%.</strong></p>
<p>- Вы абсолютно правильно сказали. В Литве я спрашивал у коллег, говорят, 8%, это много. В Польше - 5%. У нас доведено расписание бюджета, сколько соберет налоговая, сколько таможенная администрация. Так вот, таможня - это 48% от общего показателя ГФС.</p>
<p><strong>- Это сугубо украинский перекос?</strong></p>
<p>- Это постсоветский синдром плюс экономические реалии. По такому принципу работает российская таможня, белорусская таможня. Таможенные органы не должны заниматься фискальной функцией. Фискальная функция должна быть производной от защитной и сервисной функции.</p>
<p>Почему я выступаю за укрепление силовой составляющей таможенных органов? Таможенники должны заниматься границей, достоверностью товара, а не выставлять таможенную стоимость. Мы должны создать систему нормального аудита, чтобы товар поступал в страну беспрепятственно.</p>
<p>У нас нет оконченного устройства - рассчетно-кассового аппарата. То есть, к сожалению, контроля прохождения товара нет, от границы до потребителя. Вот у американцев интересная конструкция: таможенное ведомство совмещено с пограничным. У них единое агентство, и оно занимается охранными функциями: борьбой с незаконной миграцией, наркотрафиком.</p>
<p>Налоговая администрация занимается внутренними налогами. Мне симпатична модель, когда таможня занимается границей, защитной функцией: оружие не пропустить, наркотики не пропускать. Остальное пусть заходит в страну. А дальше налоговая должна контролировать и облагать налогом.</p>
<p><strong>- Можно ли перепоручить НДС налоговой службе?</strong></p>
<p>- В наших постсоветских условиях лучше всего товар поймать на входе. Вот увидели на въезде, когда декларация есть, когда фура стоит с товаром, и обложили налогом. У нас еще эта болезнь из совкового прошлого, боимся что если отпустим, то потеряем. Да, может какую-то часть товара потеряем. Однако на обороте налогов получим больше.</p>
<p><strong>- Большая часть денег исчезнет в "налоговых ямах".</strong></p>
<p>- Это тема для другого большого разговора. Согласен с вами. Почему таможня и получила "дорезку" к бюджетному заданию - плюс 48% по текущему году.</p>
<p><strong>- Это реалистично?</strong></p>
<p>- Из них 17,6% должен быть импортный сбор и плюс наше администрирование. С учетом того, как полетел сейчас доллар и посыпалась гривна, - реалистично. Я объективно себя оцениваю, никогда не оцениваю показатель работы в гривне. Мои подчиненные всегда пересчитывают в доллары, чтобы себе правду говорить.</p>
<p>Мое виденье: бюджету нужны деньги, мы это понимаем. Второе: в таможне с июля мы ни разу не сорвали выполнение бюджетного задания.</p>
<p><strong>- В том числе, из-за курса доллара?</strong></p>
<p>- Не в том числе. Я это анализировал. Когда таможне пытались предъявить этот аргумент на комитете некоторые спрашивающие, я не стал их "нагружать".</p>
<p>Но вас "нагружу" цифрами.</p>
<p>Чтобы отсечь субъективный фактор курса, все определяем в долларах. Было собрано 3,458 млрд долл. Падение на 25% по сравнению с 2013 годом.</p>
<p>Дальше я говорю: в конце 2013 года были списаны все деньги, которые были на &nbsp;депозитных счетах таможенных органов. Они были заплачены в бюджет. То есть это не было реальным выполнением, вперед залезли в карман субъекту внешнеэкономической деятельности, таможенным органам.</p>
<p>Сейчас на депозите 2 774 млн грн. Это деньги, которые субъекты заводят на счета таможен, а груз приходит завтра, послезавтра, через месяц. Они этими деньгами платят таможенные налоги в бюджет. Это не деньги бюджета, а тогда, в 2013 году, их взяли и списали с таможенных счетов.</p>
<p>Я считаю, что таможенное оформление не прошло, и я их изымаю с расчета. Понимаете, о чем я говорю? Становится минус 24%. Дальше. У нас идет Крым. В результате - минус 23%. Дальше восточные регионы. Считаем в долларах.</p>
<p><strong>- Сколько потеряли по Крыму? Какова была доля Крыма в общем балансе?</strong></p>
<p>- Минус 2,973 млрд. По востоку потеряли 2,926 млрд. Выходим на минус 19%. Подчеркиваю: в долларе - на 19% по сравнению с 2013 годом мы заработали меньше, если вычесть все. Объемы импорта упали на 21%. Это объемы по стоимости, не по весу. Платежи упали на 19%.&nbsp;Сопоставимо?</p>
<p>Администрируем неплохо, хотя упала стоимость в долларах на многие товары, то есть база налогообложения. 4 млрд потеряли только на налогах на легковые автомобили.</p>
