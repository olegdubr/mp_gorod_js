<p>- Россияне потребовали перерегистрацию, но Закон о СЭЗ "Крым" не дает нам оснований для запрета ввоза в Украину&nbsp; товаров, произведенных в этой СЭЗ. Это одна из коллизий закона о СЭЗ "Крым". У нас есть предложения, но мы, к сожалению, не обладаем правом законодательной инициативы.</p>
