<p><strong>- Вы давно внесли его кандидатуру?</strong></p>
<p>- Две недели назад <em>(примерно в середине января &ndash; ЭП).</em></p>
<p><strong>- А когда появилась новость?</strong></p>
<p>- Через неделю, после официального релиза комитета.</p>
<p><strong>- Схема Курченко устранена, но сильно упало оформление прочего импорта. Перед вашим приходом, когда таможню курировал Виталий Науменко, оно начало падать. О чем это говорит? О потере управляемости?</strong></p>
<p>- Виталий Петрович Науменко - профессиональный таможенник. Может, ему не хватило той авторитарности, которая есть во мне, и определенной жесткости. Плюс время ему досталось неблагополучное. Таможня была в переходном состоянии.</p>
<p>Кстати, Виталий Петрович проявил жесткость: он уволил многих начальников таможни, но многих не назначил. У него не было такого права. Надо было на кого-то возлагать ответственность, а получилось междуцарствие. В этом междуцарствии было ощущение последнего дня.</p>
<p>Я почувствовал то же, когда пришел сюда. Как бывший начальник таможни, ментальность этих бойцов знаю очень хорошо. Я своих коллег тут посадил и кое-что сказал. Потом мы начали общаться с бизнесом, учиться слушать друг друга. Спросили, сколько вас заставляют загонять в кэш? Вот сколько. Не будьте барыгами, переверните пирамиду. В ряде случаев это удалось.</p>
<p><strong>- В чем был смысл двоевластия? Науменко еще оставался, а вы уже пришли.</strong></p>
<p>- Не было никакого двоевластия, там документально все понятно произошло. У нас были нормальные разумные отношения. Виталий Науменко нас сейчас критикует и правильно делает: таможню есть за что критиковать и меня в том числе.</p>
<p>Повторяю, что Виталий Петрович - человек профессиональный, имеющий свою точку зрения, и к его точке зрения отношусь с уважением. Он ушел из таможенных органов, но это человек, который объективно оценивает ситуацию.</p>
<p>Подчеркиваю: никакого двоевластия и закулисных игр не было.</p>
