<p>- Схема вывоза товара похожа на схему по экспортно-импортной операции.</p>
<p><strong>- Просто называется юридически по-другому?</strong></p>
<p>- Да, юридически это называется "Закон про вільну економічну зону "Крим".</p>
<p><strong>- Но деньги в бюджет поступают?</strong></p>
<p>- Деньги поступают в бюджет Украины.</p>
<p><strong>- Крупные крымские предприятия - "Титан", Содовый завод, "Черноморнефтегаз" - должны ввозить большие партии товаров&hellip;</strong></p>
<p>- У них не было претензий к нам. Была проблема с вывозом их товара сюда, например, соды.</p>
