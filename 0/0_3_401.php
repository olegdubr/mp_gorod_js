<p><strong>- Говорят, через границу с зоной АТО идут сумасшедшие потоки товаров, в частности вагоны с углем. Это оправдано: предприятия, которые там работают, должны, условно говоря, платить налоги и там, и здесь, и иного выхода, кроме как делать все в кэше, они не видят.</strong></p>
<p>- У нас только одна железнодорожная точка выхода оттуда: Красный Лиман, где мы проверяем каждый вагон и документы. Создано главное управление, которое будет заниматься проблемами зоны АТО.</p>
<p>В состав управления входят сотрудники налоговой милиции. Эти эти люди начнут выполнять полубоевую задачу по контролю товаров, которые будут проходить через установленные транспортные коридоры. Эти люди будут там находиться и нести службу на постоянной основе. Вооружены и экипированы соответственно.</p>
<p>Нам приходит информация, что резко возросли суммы взяток. Почему? Начали держать товар - дороже стало проезжать. Проблема в том, что пытаются в ночное время проехать через окольные тропы, пытаются купить.</p>
<p><strong>- Говорят, буквально на полчаса ночью открывается коридор, и через него без остановок проходит масса эшелонов.</strong></p>
<p>- Эшелон не может пройти, потому что там одна ветка. Автоколонна - я не исключаю. Там контроль только в дневное время ведут сотрудники налоговой милиции из донецкого и луганского главков.</p>
<p>Мы укомплектовали подразделение, которое прошло подготовку на полигоне. Их готовили к боевой задаче. Это добровольцы, что немаловажно. Они поедут туда. Будем усиливать. Генерал Хоменко очень "плотно" занимается этим вопросом. Ситуация реально меняется.</p>
<p><strong>- Может, Мухин кому-то "перешел дорогу"?</strong></p>
<p>- Мухин - офицер, "афганец", имеет боевой опыт, работал в Службе безопасности Украины, генерал Налоговой милиции, был заместителем председателя Таможенной службы в моей каденции, когда я был председателем Таможенной службы Украины.</p>
<p>Я этому человеку доверяю. Этот человек имеет принципы, достаточно жесткий человек, я бы сказал, чрезмерно жесткий. И то, что он перешел дорогу&hellip;</p>
