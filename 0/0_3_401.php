<p><strong>- А газ учитывается в этих расчетах?</strong></p>
<p>- Только если он импортируется не НАК "Нефтегаз Украины". Здесь абсолютно честные цифры, потому что здесь весь налогооблагаемый импорт. Показано лишь то, что облагалось налогом. Логика понятна?</p>
<p>Себе я должен говорить правду: ты хорошо сработал или нет. Где-то на четверочку с большим минусом, потому что ресурс есть. А если, честнее на "тройку". Но это и не "неуд".</p>
<p><strong>- Сколько в общем проценте добавило пресечение схемы Курченко?</strong></p>
<p>- 9 млрд грн. Это по первому полугодию.</p>
<p><strong>- Я о том, что потом появились объемы Курченко. Получается, реальное падение остального импорта и поступлений от него гораздо больше?</strong></p>
<p>- Фактически да.</p>
<p><strong>- Оно трактуется так, что выросла контрабанда остальных товаров.</strong></p>
<p>- А теперь о контрабанде. Была таможня - были контрабандисты.</p>
<p>Есть у меня две интересные таблички. Это тоже таблицы, которые не готовятся к вашему приходу, их мне кладут ежедневно. Это 40-футовые контейнеры, которые считаются наиболее интересными для "черного" товара. 40-футовый контейнер - это который во всю фуру стает, вы видите, как их перевозят.</p>
<p>Так вот: в первом квартале вес товаров в одном контейнере был 12,6 тонны, сегодня - 15,2 тонны. На протяжении 2012-2013 годов товара было 12,7-13,6 тонны. Сегодня мы вышли на 15,2 тонны. О чем это говорит? Мы нашли три тонны.</p>
<p>Посчитайте, сколько мы добавили налогооблагаемого веса в контейнере, сколько начали достоверно декларировать. Я не говорю, что там все хорошо, но за этот период, за год, в среднем 3 тонны товара реально добавили в одном контейнере.</p>
<p>Сколько брали налогов с контейнера в 2013 году? 9 тыс долл с одного. Сейчас берем 13 тыс долл. Это серьезно. На мой взгляд, это показатель.</p>
<p style="text-align: center;">***</p>
<p><strong>- Падение общих объемов может быть признаком увеличения контрабанды?</strong></p>
<p>- Нет.</p>
<p><strong>- Сопоставляли цифры? </strong></p>
<p>Сопоставим Украину и Польшу. Данные за девять месяцев. Сверку с польской таможенной администрацией за год еще не делали. У нас разночтение на 80 млн долл.</p>
<p>Разночтение легко проверяется. Садятся специалисты двух таможенных администраций и сводят цифры.</p>
<p><strong>- Рассказывают, что вертикаль была, никто не отрицает. Когда во время Майдана она разрушилась, в локальных скоплениях таможни - одесском и западном регионах - люди стали принимать все деньги на себя. Более того, таможни начали конкурировать друг с другом, предлагая скидки. Было такое?</strong></p>
<p>- Это правда. После того, как сломали стройную, понятную для всяких "барыг" вертикаль, когда они знали, что все упирается в киевское небо, это небо рассыпалось на маленькие хрусталики и упало в каждый отдельный регион.</p>
<p>Перед вами сидит человек, который делает непопулярные предложения. Я не принимаю кадровые решения, к сожалению. У меня нет этого права, могу только предлагать. И вот я предложил. При моей каденции пятый начальник таможни поехал в Одессу. Они не успевают прийти в себя, как я предлагаю их там поменять.</p>
<p>Местные элиты пытаются управлять начальником таможни, и не у каждого выдерживает, скажем так, характер. Это первая причина. Вторая причина - мы не можем назначить начальника таможни без согласования с главой ОГА.</p>
<p><strong>- Вот и мотивация для появления смс.</strong></p>
<p>- Без комментариев. Такая реальная ситуация. Были заблокированы серьезные назначения по ряду таможен.</p>
<p>На Одесскую таможню рекомендовал Владимира Передерия. Человека, которого знаю давно. Это профессиональный жесткий таможенник. Я сказал, что пойду к председателю, буду настаивать, чтобы с ним туда направили людей.</p>
