<p><strong>- Мухин пришел с вами?</strong></p>
<p>- Да. Я ассоциирую свое имя с его именем, думаю, он не будет против. Кстати, когда написали это смс, шла переписка и раскручивалась вся история, он находился на востоке Украины. Он один из тех, кто занимается линией разграничения.</p>
